package Domain;

public class CommentVo {

	private Number fbidx;
	private Number cmidx;
	private String cmwriter;
	private String cmcomment;
	
	
	public Number getFbidx() {
		return fbidx;
	}
	public void setFbidx(Number fbidx) {
		this.fbidx = fbidx;
	}
	public Number getCmidx() {
		return cmidx;
	}
	public void setCmidx(Number cmidx) {
		this.cmidx = cmidx;
	}
	public String getCmwriter() {
		return cmwriter;
	}
	public void setCmwriter(String cmwriter) {
		this.cmwriter = cmwriter;
	}
	public String getCmcomment() {
		return cmcomment;
	}
	public void setCmcomment(String cmcomment) {
		this.cmcomment = cmcomment;
	}
	
	
}
